package com.example.foodmania.Api_Models;

public class APINotifications {

    String content, f_name, l_name, encodedprofile;

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getF_name() {
        return f_name;
    }

    public void setF_name(String f_name) {
        this.f_name = f_name;
    }

    public String getL_name() {
        return l_name;
    }

    public void setL_name(String l_name) {
        this.l_name = l_name;
    }

    public String getEncodedprofile() {
        return encodedprofile;
    }

    public void setEncodedprofile(String encodedprofile) {
        this.encodedprofile = encodedprofile;
    }
}
